<h1 class="font-title font-bold text-3xl text-slate-900 mb-4">Privātuma politika</h1>

<h3 class="font-title font-bold text-xl text-slate-800 mt-4">Jūsu dati</h3>
<p>Mēs saglabājam tikai un vienīgi laikus, kad jūs apmeklējāt šo saiti. Nekādi citi dati netiek saglabāti.</p>

<h3 class="font-title font-bold text-xl text-slate-800 mt-4">Sīkdatnes <i>(cookies)</i></h3>
<p>Sīkdatnes ir neliels daudzums datu, ko mājaslapa var saglabāt, piemēram, mājaslapas iestatījumi. Sīkdatnes ir gandrīz vienīgais veids, kā mājaslapa var jūs
    atcerēties un zināt, ka jūs esat jūs.</p>
<p>Mēs sīkdatnes izmantojam pārsvarā statistiskiem nolūkiem, bet arī dažām funkcionālām daļām.</p>
<p>Ieejot šajā mājaslapā, Jums parādās izvēlne lapas apakšā - vai jūs vēlaties piekrist vai nepiekrist sīkdatnēm. Ja jūs nepiekrītat,
    tad tiks saglabāta tikai viena sīkdatne, lai Jums šo izvēlni vairs nerādītu, un mēs nekādas citas sīkdatnes nesaglabāsim.</p>

<h3 class="font-title font-bold text-xl text-slate-800 mt-4">Statistika</h3>
<p>Vienīgie statistiskie dati, kas tiek ievākti, ir laiki, kad jūs apmeklējāt šo saiti, kā arī, ja jūs atļaujat sīkdatnes, īpašs ID, kas tiek saglabāts
    kā sīkdatne. Katram apmeklētājam šis ID ir atšķirīgs, un mēs to izmantojam, lai atšķirtu apmeklētājus.</p>
<p>Visi statistikas dati tiek neatgriezeniski dzēsti 7 dienas pēc to ievākšanas.</p>